    private void btnAsignarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarEActionPerformed
        String[] info = new String[3];
        info[0] = txtNombre.getText();
        info[1] = txtApellido.getText();
        info[2] = txtCedula.getText();
        modelo.addRow(info);
        String cedula = txtCedula.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        alumno = new Alumno(nombre, cedula, apellido);
        controladorAlumno.create(alumno);
        try {
            controladorAlumno.guardar();
        } catch (IOException ex) {
            Logger.getLogger(VentanaDocentesAsignados.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(this, "alumno creado");

        txtNombre.setText("");
        txtApellido.setText("");
        txtCedula.setText("");
    }//GEN-LAST:event_btnAsignarEActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String curso = txtCurso.getText();
        String materia = txtMateria.getText();
        if (curso.isEmpty() || materia.isEmpty()) {
            JOptionPane.showMessageDialog(this, "falta campos");
        } else {
            controladorCurso.crearA(curso, materia);
            controladorCurso.ingreesarRegex("\\/store\\/apps\\/details\\?id=com\\.(.[^\\\"\\s])+");
            StringBuilder stringBuilder = new StringBuilder();
            try {
                URL urlObject = new URL("https://play.google.com/store/search?q=" + materia.
                        replaceAll("//s", "//+") + "&c=apps");
                URLConnection uRLConnection = urlObject.openConnection();
                uRLConnection.setRequestProperty("User-Agent", "Mozilla/5.0(Windows NT 6.1; WOW64) AppleWebKit/537.11 (KHTML, like Gecko)Chrome/23.0.1271.95 Safari/537.11");
                BufferedReader bufferedReader = new BufferedReader(new InputStreamReader(uRLConnection.getInputStream(),
                        "UTF-8"));
                String inputLine;
                while ((inputLine = bufferedReader.readLine()) != null) {
                    stringBuilder.append(inputLine);
                }
            } catch (IOException ex) {

            }
            DefaultListModel mo = new DefaultListModel();
            Set<String> resp = controladorCurso.obtenerURLGoogle(stringBuilder.toString());
            resp.stream().forEach(s -> mo.addElement("https://play.google.com".concat(s).concat("--->")
                    .concat(s.substring(s.lastIndexOf(".") + 1, s.length()))));
            jList1.setModel(mo);
            txtMateria.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


